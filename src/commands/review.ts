import chalk from 'chalk';
import ora from 'ora';
import { promises as fs } from 'fs';
import { join } from 'path';
import { loadConfig, getClaudeApiKey } from '../utils/config';
import { fetchBranches, generateDiff, branchExists } from '../utils/git';
import { ClaudeService } from '../utils/claude';
import { CLIOptions } from '../types';

export async function reviewCommand(
  sourceBranch: string,
  targetBranch: string,
  options: CLIOptions
): Promise<void> {
  console.log(chalk.blue(`🔍 Generating code review: ${sourceBranch} → ${targetBranch}`));

  // Load configuration
  const config = await loadConfig();
  const language = options.language || config?.language || 'en';
  const outputFile = options.output || 'code-review.md';

  // Validate API key
  const apiKey = getClaudeApiKey();
  if (!apiKey) {
    console.error(chalk.red('❌ Claude API key not found.'));
    console.error(chalk.red('   Please set CLAUDE_API_KEY or ANTHROPIC_API_KEY environment variable.'));
    console.error(chalk.red('   Or run: ccr init'));
    process.exit(1);
  }

  let spinner = ora('Validating branches...').start();

  try {
    // Check if branches exist
    const [sourceExists, targetExists] = await Promise.all([
      branchExists(sourceBranch),
      branchExists(targetBranch)
    ]);

    if (!sourceExists) {
      spinner.fail(`Source branch '${sourceBranch}' not found`);
      process.exit(1);
    }

    if (!targetExists) {
      spinner.fail(`Target branch '${targetBranch}' not found`);
      process.exit(1);
    }

    spinner.succeed('Branches validated');

    // Fetch branches
    spinner = ora('Fetching branches from remote...').start();
    await fetchBranches(sourceBranch, targetBranch);
    spinner.succeed('Branches fetched successfully');

    // Generate diff
    spinner = ora('Generating diff...').start();
    const diffFile = `diff-${sourceBranch}-${targetBranch}.patch`;
    const diff = await generateDiff({
      sourceBranch,
      targetBranch,
      outputFile: diffFile
    });
    
    if (!diff.trim()) {
      spinner.warn('No differences found between branches');
      return;
    }
    
    spinner.succeed(`Diff generated: ${diffFile}`);

    // Generate code review with Claude
    spinner = ora('Analyzing code with Claude AI...').start();
    const claudeService = new ClaudeService(apiKey);
    const review = await claudeService.generateCodeReview(diff, language);
    spinner.succeed('Code review generated');

    // Save review to file
    spinner = ora('Saving review...').start();
    const reviewContent = `# Code Review Summary

**Branches:** \`${sourceBranch}\` → \`${targetBranch}\`  
**Generated:** ${new Date().toLocaleString()}  
**Language:** ${language.toUpperCase()}

---

${review.details}

---

*Generated by CCR (Claude Code Review) - ${review.timestamp}*
`;

    await fs.writeFile(join(process.cwd(), outputFile), reviewContent);
    spinner.succeed(`Review saved to: ${outputFile}`);

    // Clean up diff file
    await fs.unlink(join(process.cwd(), diffFile));

    console.log(chalk.green('\n✨ Code review completed successfully!'));
    console.log(chalk.cyan(`📄 Review file: ${outputFile}`));
    
    if (review.suggestions.length > 0) {
      console.log(chalk.yellow('\n💡 Key suggestions found:'));
      review.suggestions.slice(0, 3).forEach((suggestion, i) => {
        console.log(chalk.yellow(`   ${i + 1}. ${suggestion}`));
      });
    }

  } catch (error) {
    spinner.fail('Failed to generate code review');
    console.error(chalk.red('Error details:'), error);
    process.exit(1);
  }
}